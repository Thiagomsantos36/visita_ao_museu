ACESSIBILIDADE
O Museu ainda não está adequadamente adaptado para atender a contento pessoas com deficiências. Possuimos alguns equipamentos adaptados como banheiros, equipamento móvel (que permite acoplar cadeiras de rodas para vencer desníveis e escadas) e algumas iniciativas expositivas. Ainda não concluímos as ações e projetos para instalação de rampas e elevadores próprios e exclusivos para este fim, permitindo de forma autônoma a acessibilidade para este público. Estamos trabalhando para atingir estas metas com a possível brevidade.
 </P>

<P>
AGENDAMENTO DE VISITA
Para agendamento de grupos escolares, ONG's, etc, favor entrar em contato com o Núcleo de Atendimento ao Público - NAP pelo E-mail: agendamento.nap@mn.ufrj.br.

A agenda do Museu Nacional inicia-se sempre no primeiro dia (dia 01) do mês vigente, abrindo vagas de visitação agendadas até o último dia do mês subsequente. Sendo assim, pode-se agendar uma visita em grupo com no máximo 2 meses de antecedência.

Lembramos que o agendamento de um grupo não implica em uma visita mediada por guia.

Devido ao corte de todas as bolsas dos estudantes de graduação que atuavam no Setor Educativo - SAE do Museu Nacional como mediadores, o Setor Educativo não poderá mais se comprometer com a realização de visitas mediadas às 10h e 14h, de terça a sexta-feira, como vinha acontecendo. O Setor Educativo continuará se esforçando para atender as escolas por meio da atuação de seus servidores e de alguns estagiários voluntários, contudo não pode mais garantir a implementação das visitas. A SAE e o Museu Nacional lamentam muito e pede desculpas às escolas e aos docentes pelos possíveis transtornos causados.
</P>
