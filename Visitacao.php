* Alunos de escolas públicas ou particulares não acompanhados de professores deverão trazer ofício da escola, justificando 'visita de estudo' e estarão sujeitos às normas estabelecidas para escolas públicas e particulares.

** Escolas particulares, com alunos uniformizados e acompanhados por professores, previamente agendadas no Núcleo de Atendimento ao Público - NAP, pagam R$ 3,00 por aluno e o mesmo valor para cada acompanhante em um grupo de 10 alunos. Os acompanhantes excedentes pagam entrada inteira ou meia de acordo com a tabela de valores de ingressos. É necessário um acompanhante para cada grupo de 10 alunos (solicita‐se agendamento prévio);

** Colônias de férias promovidas por órgãos públicos pagam R$ 3,00 por cada participante nos dias úteis (solicita‐se agendamento prévio); Não haverá entrada gratuita aos sábados, domingos e feriados nacionais, exceto para escolas públicas noturnas e/ou de outras cidades/estados, mediante agendamento prévio com o Núcleo de Atendimento ao Público - NAP; Em caso de impossibilidade da visita, tal fato será comunicado à escola, caso seja fornecido o telefone para contato.

OBSERVAÇÕES
• Menores de 21 anos – devem apresentar documento de Identidade (RG) com foto, ou certidão de nascimento quando acompanhado dos pais.

• Estudantes – devem apresentar carteira de identificação estudantil expedida por escolas da rede pública ou particular de ensino fundamental, médio e superior (graduação, pós‐ graduação, mestrado ou doutorado); UNE, FESN (só aplicável para estudantes de estabelecimentos oficiais de ensino fundamental, médio e superior).

• Na falta da carteirinha apresentar documento com foto juntamente com o boleto pago do mês corrente, ou atestado de escolaridade, e frequência em papel timbrado do ano/semestre corrente. No caso de escola pública, apresentar atestado de escolaridade, frequência em papel timbrado do ano/semestre corrente e documento com foto. Só o Boleto bancário não serve como documento de identificação estudantil.

• Caso a carteirinha de estudante não conste a data de validade, deverá ser apresentado documento que comprove a matrícula ou a frequência no ano letivo em curso. Idosos – devem apresentar documento de Identidade (RG) com foto.

